# Show different data types and how to check them

x = 10          # int
y = 3.14        # float
z = "Python"    # str
a = [1, 2, 3]   # list
b = (4, 5, 6)   # tuple
c = {7, 8, 9}   # set
d = {"name": "Guna", "age": 21}  # dict
e = True        # bool

print(type(x), x)
print(type(y), y)
print(type(z), z)
print(type(a), a)
print(type(b), b)
print(type(c), c)
print(type(d), d)
print(type(e), e)