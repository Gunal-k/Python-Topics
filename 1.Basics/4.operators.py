# Demonstrate arithmetic, comparison, and logical operators

# Arithmetic
a = 10
b = 3
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a // b =", a // b)  # floor division
print("a % b =", a % b)    # modulus
print("a ** b =", a ** b)  # exponentiation

# Comparison
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)

# Logical
x = True
y = False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)